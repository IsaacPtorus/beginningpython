# Arithmetic operators
# Simple calculations
num1 = 85
num2 = 45
print(num1 + num2)
print(num1 - num2)
print(num1 / num2)
print(num1 * num2)
print(num1 % num2)  #modulus----returns the rem. after division

# Comparison operators---used to compare values
print(num1 > num2)
print(num1 < num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2)  #Equal to...
print(num1 != num2)  #Not Equal to...

# Assignment operators----assign values to variables
x = 457
print(x)

x += 65
print(x)

# Octal numbers
print(0O100000)
print(0O100101)
print(0O100111)

y = 67
y -= 6
print(y)

# Logic operators----and,or and not
a = 23
print(a < 40 and a < 2)
print(a < 40 or a < 2)
print(not (a < 40 or a < 2))

# Operator precedence ---order of operation execution
result = (10 + 6 / 2 - 4)
print(result)
print(int(result))
